package com.moneyManager;

public class DoMath {
    public int addNumbers (Integer firstNumber, Integer secondNumber){
        return firstNumber + secondNumber;
    }

    public int subtractNumbers (Integer firstNumber, Integer secondNumber){
        return firstNumber - secondNumber;
    }

    public int divideNumbers (Integer firstNumber, Integer secondNumber){
        return firstNumber/secondNumber;
    }

    public int multiplyNumbers (Integer firstNumber, Integer secondNumber){
        return firstNumber * secondNumber;
    }
}
